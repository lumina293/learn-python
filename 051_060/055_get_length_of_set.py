set1 = {'banana', 'cherry', 'apple'}
print(set1)

# length set len()
length = len(set1)
print("The length of set is: ", length)

set2 = {'a', 'b', 'c', 'a'}
print(set2)
length = len(set2)
print(length)

set3 = {'s', 'f', '3', 4}
print(set3)
length = len(set3)
print(length)

set4 = {'s', 'f', '@', 4}
print(set4)
length = len(set4)
print(length)
